    public static string Get(CultureInfo culture, string key, params object[] args)
    {
        var table = culture.TwoLetterISOLanguageName.Equals("en", StringComparison.OrdinalIgnoreCase)
            ? English
            : Dutch;

        if (!table.TryGetValue(key, out var value) && !Dutch.TryGetValue(key, out value))
        {
            value = key;
        }

        return args.Length == 0 ? value : string.Format(culture, value, args);
    }

    public static string T(string key, params object[] args)
    {
        return Get(CultureInfo.CurrentUICulture, key, args);
    }
